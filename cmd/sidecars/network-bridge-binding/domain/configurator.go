package domain

import (
	"encoding/xml"
	"fmt"
	"kubevirt.io/kubevirt/pkg/virt-launcher/virtwrap/device"

	vmschema "kubevirt.io/api/core/v1"

	domainschema "kubevirt.io/kubevirt/pkg/virt-launcher/virtwrap/api"

	"kubevirt.io/client-go/log"

	//	"kubevirt.io/kubevirt/pkg/virt-launcher/virtwrap/device"

	"kubevirt.io/kubevirt/pkg/network/vmispec"
)

type NetworkConfiguratorOptions struct {
	UseVirtioTransitional bool
	Mac                   string
}

type BridgeNetworkConfigurator struct {
	vmiSpecIface *vmschema.Interface
	options      NetworkConfiguratorOptions
}

const (
	// BridgePluginName bridge binding plugin name should be registered to Kubevirt through Kubevirt CR
	BridgePluginName = "bridge"
)

func NewBridgeNetworkConfigurator(ifaces []vmschema.Interface, networks []vmschema.Network, opts NetworkConfiguratorOptions) (*BridgeNetworkConfigurator, error) {
	network := vmispec.LookupPodNetwork(networks)
	if network == nil {
		return nil, fmt.Errorf("pod network not found")
	}
	iface := vmispec.LookupInterfaceByName(ifaces, network.Name)

	if iface == nil {
		return nil, fmt.Errorf("no interface found")
	}

	if iface.Binding == nil || iface.Binding != nil && iface.Binding.Name != BridgePluginName {
		return nil, fmt.Errorf("interface %q is not set with Bridge network binding plugin", network.Name)
	}

	// If VMI doesn't have static MAC address, use an autogenerated
	if opts.Mac != "" && iface.MacAddress == "" {
		iface.MacAddress = opts.Mac
	}

	log.Log.Infof("Attaching TAP with MAC: %s", opts.Mac)

	return &BridgeNetworkConfigurator{
		vmiSpecIface: iface,
		options:      opts,
	}, nil
}

func (p BridgeNetworkConfigurator) Mutate(domainSpec *domainschema.DomainSpec) (*domainschema.DomainSpec, error) {
	generatedIface, err := p.generateInterface()
	if err != nil {
		return nil, fmt.Errorf("failed to generate domain interface spec: %v", err)
	}

	domainSpecCopy := domainSpec.DeepCopy()
	if iface := lookupIfaceByAliasName(domainSpecCopy.Devices.Interfaces, p.vmiSpecIface.Name); iface != nil {
		*iface = *generatedIface
	} else {
		domainSpecCopy.Devices.Interfaces = append(domainSpecCopy.Devices.Interfaces, *generatedIface)
	}

	xmlData, err := xml.MarshalIndent(generatedIface, "", "  ")
	if err != nil {
		log.Log.Infof("Error serializing interface to XML: %v", err)
	}

	log.Log.Infof("bridge interface is added to domain spec successfully: %s", string(xmlData))

	return domainSpecCopy, nil
}

func lookupIfaceByAliasName(ifaces []domainschema.Interface, name string) *domainschema.Interface {
	for i, iface := range ifaces {
		if iface.Alias != nil && iface.Alias.GetName() == name {
			return &ifaces[i]
		}
	}

	return nil
}

func (p BridgeNetworkConfigurator) generateInterface() (*domainschema.Interface, error) {
	var pciAddress *domainschema.Address
	if p.vmiSpecIface.PciAddress != "" {
		var err error
		pciAddress, err = device.NewPciAddressField(p.vmiSpecIface.PciAddress)
		if err != nil {
			return nil, err
		}
	}

	var ifaceModel string
	if p.vmiSpecIface.Model == "" {
		ifaceModel = vmschema.VirtIO
	} else {
		ifaceModel = p.vmiSpecIface.Model
	}

	var ifaceModelType string
	if ifaceModel == vmschema.VirtIO {
		if p.options.UseVirtioTransitional {
			ifaceModelType = "virtio-transitional"
		} else {
			ifaceModelType = "virtio-non-transitional"
		}
	} else {
		ifaceModelType = p.vmiSpecIface.Model
	}
	model := &domainschema.Model{Type: ifaceModelType}

	var mac *domainschema.MAC
	if p.vmiSpecIface.MacAddress != "" {
		mac = &domainschema.MAC{MAC: p.vmiSpecIface.MacAddress}
	}

	var acpi *domainschema.ACPI
	if p.vmiSpecIface.ACPIIndex > 0 {
		acpi = &domainschema.
			ACPI{Index: uint(p.vmiSpecIface.ACPIIndex)}
	}

	const (
		ifaceType = "ethernet"
	)

	return &domainschema.Interface{
		Alias:   domainschema.NewUserDefinedAlias("default"),
		Model:   model,
		Address: pciAddress,
		MAC:     mac,
		MTU:     &domainschema.MTU{Size: "1480"},
		ACPI:    acpi,
		Type:    ifaceType,
		Rom:     &domainschema.Rom{Enabled: "no"},
		Target:  &domainschema.InterfaceTarget{Device: "tap0", Managed: "no"},
	}, nil
}
